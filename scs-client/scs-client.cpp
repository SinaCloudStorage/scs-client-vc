// CscsclientApp 初始化

BOOL CscsclientApp::InitInstance()
{
	CWinApp::InitInstance();


	// 创建 shell 管理器，以防对话框包含
	// 任何 shell 树视图控件或 shell 列表视图控件。
	CShellManager *pShellManager = new CShellManager;

	// 激活“Windows Native”视觉管理器，以便在 MFC 控件中启用主题
	CMFCVisualManager::SetDefaultManager(RUNTIME_CLASS(CMFCVisualManagerWindows));

	// 标准初始化
	// 如果未使用这些功能并希望减小
	// 最终可执行文件的大小，则应移除下列
	// 不需要的特定初始化例程
	// 更改用于存储设置的注册表项
	// TODO: 应适当修改该字符串，
	// 例如修改为公司或组织名
	SetRegistryKey(_T("应用程序向导生成的本地应用程序"));

#ifdef RECORD_LOG
	std::wstring cur_dir = L"c:\\";
	WCHAR modulePath[MAX_PATH];
	if ( ::GetModuleFileName( NULL, modulePath, MAX_PATH ) )
	{
		cur_dir = modulePath;
		std::wstring::size_type p = cur_dir.rfind(L'\\');
		if ( p != std::wstring::npos )
		{
			cur_dir = cur_dir.substr( 0, p+1 );
		}
	}
	std::wstring temp = cur_dir + L"log4cplus.prop";
	LOG4CPLUS_INIT(temp.c_str());

//	AllocConsole();
#endif

	singleton_ex<multi_http>::create_instance();

	CscsclientDlg dlg;
	m_pMainWnd = &dlg;
	INT_PTR nResponse = dlg.DoModal();
	if (nResponse == IDOK)
	{
		// TODO: 在此放置处理何时用
		//  “确定”来关闭对话框的代码
	}
	else if (nResponse == IDCANCEL)
	{
		// TODO: 在此放置处理何时用
		//  “取消”来关闭对话框的代码
	}
	else if (nResponse == -1)
	{
		TRACE(traceAppMsg, 0, "警告: 对话框创建失败，应用程序将意外终止。\n");
		TRACE(traceAppMsg, 0, "警告: 如果您在对话框上使用 MFC 控件，则无法 #define _AFX_NO_MFC_CONTROLS_IN_DIALOGS。\n");
	}

	// 删除上面创建的 shell 管理器。
	if (pShellManager != NULL)
	{
		delete pShellManager;
	}

	singleton_ex<multi_http>::destroy_instance();

#ifdef RECORD_LOG
//	FreeConsole();
#endif

	// 由于对话框已关闭，所以将返回 FALSE 以便退出应用程序，
	//  而不是启动应用程序的消息泵。
	return FALSE;
}

